public class SequenciaFibonnaci {
    public static void main(String[] args) {
        /*
        A série de Fibonacci é uma sequência de números onde cada número é a soma dos dois anteriores.
         A sequência começa com 1 e 1, e os próximos termos são gerados da seguinte forma:
         */
        for (int i = 1; i <=15 ; i++) {
            for (int j = 1; j <=15 ; j++) {
                System.out.println(i+j);
                
            }
            
        }
    }
}
