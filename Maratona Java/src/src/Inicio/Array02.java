package Inicio;

public class Array02 {
    public static void main(String[] args) {
        String[] nomes = new String[3];
        nomes[0] = "Rodrigo";
        nomes[1] = "Joanice";
        nomes[2] = "João";

        for (int i = 0; i < nomes.length; i++) {
            System.out.println(nomes[i]);

        }
    }
}
